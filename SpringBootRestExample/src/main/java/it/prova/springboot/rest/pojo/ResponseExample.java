package it.prova.springboot.rest.pojo;

public class ResponseExample {

	private String exitCode;

	private String propertyLetta;

	private String exitBody;

	public String getPropertyLetta() {
		return propertyLetta;
	}

	public void setPropertyLetta(String propertyLetta) {
		this.propertyLetta = propertyLetta;
	}

	public String getExitCode() {
		return exitCode;
	}

	public void setExitCode(String exitCode) {
		this.exitCode = exitCode;
	}

	public String getExitBody() {
		return exitBody;
	}

	public void setExitBody(String exitBody) {
		this.exitBody = exitBody;
	}

}
